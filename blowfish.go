func NewBlowfishCBCCryptor(aesKey, iv []byte, padding padding) (ICryptor, error) {
	cipher, err := blowfish.NewCipher(aesKey)
	if err != nil {
		return nil, err
	}
	return NewCBCCipher(cipher, iv, padding), nil
}
